#include <stdio.h>

int	ft_str_is_alpha(char *str)
{
	int	x;

	x = 0;
	if (!str)
		return (1);
	while (str[x])
	{
		if (str[x] < 'a' || str[x] > 'z')
		{
			if (str[x] < 'A' || str[x] > 'Z')
				return (0);
		}
		x++;
	}
	return (1);
}
