int	ft_iterative_factorial(int nb)
{
	int	i;

	if (nb < 0)
		return (0);
	if (nb == 0)
		return (1);
	i = nb;
	nb--;
	while (nb > 1)
	{
		i = i * nb;
		nb--;
	}
	return (i);
}
